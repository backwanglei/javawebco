package javabe;

public class CityBean {
  private String cityname;
  private String age;
  public CityBean(){
	  
  }
  public void setCityname(String cityname){
	  this.cityname=cityname;
  }
  public void setAge(String age){
	  this.age=age;
  }
  public String getCityname(){
	  return this.cityname;
  }
  public String getAge(){
	  return this.age;
  }
}
